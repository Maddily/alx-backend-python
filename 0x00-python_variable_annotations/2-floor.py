#!/usr/bin/env python3
"""
This module provides a function to calculate the floor value
of a given float number.
"""


def floor(n: float) -> int:
    """
    Returns the floor value of a given float number.

    Args:
        n (float): The number to calculate the floor value for.

    Returns:
        int: The floor value of the given number.
    """

    return int(n)
